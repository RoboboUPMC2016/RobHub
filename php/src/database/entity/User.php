<?php

/**
 * The class User is the object representation
 * of a user entry of the database.
 */
class User
{

    /** 
     * string The username of the user.
     */
    public $username;

    /** 
     * string The password of the user.
     */
    public $password;

    /** 
     * string The first name of the user.
     */
    public $firstname;

    /** 
     * string The last name of the user.
     */
    public $lastname;
}
?>